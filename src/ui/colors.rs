pub fn to_color(color: [u64; 4]) -> [f32; 4] {
    return [
        (color[0] as f32 / 255.0),
        (color[1] as f32 / 255.0),
        (color[2] as f32 / 255.0),
        (color[3] as f32 / 255.0),
    ];
}
